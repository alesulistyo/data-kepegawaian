    private void tampil_dashboardcount() {
        try {
            Connection con = new database.connection().configDB();
            String sql = "SELECT (SELECT COUNT(*)FROM datapegawai) AS count_pegawai,\n"
                    + "(SELECT COUNT(*)FROM usulanpangkat) AS count_pangkat,\n"
                    + "(SELECT COUNT(*)FROM usulanjabatan) AS count_jabatan\n"
                    + "FROM DUAL";
            java.sql.Statement stat = con.createStatement();
            java.sql.ResultSet hasil = stat.executeQuery(sql);

            if (hasil.next()) {
                try {
                    label_countpegawai.setText(hasil.getString("count_pegawai"));
                    label_countpangkat.setText(hasil.getString("count_pangkat"));
                    label_countjabatan.setText(hasil.getString("count_jabatan"));
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Gagal menampilkan Dashboard!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ada!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Query Error!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        }
    }

